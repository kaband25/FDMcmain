/* USER CODE BEGIN 0 */
uint16_t meArr[10000];
uint8_t cobsArr[500];
uint8_t order[8];
uint8_t order_[6];
uint8_t *pOrder = order;
uint16_t ArrI=0;
uint16_t frequency=0;
uint16_t numberOfMeasurements=0;
uint16_t numberOfRepeats=0;
/* USER CODE END 0 */

